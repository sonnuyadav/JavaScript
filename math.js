function squire(number){
	return number * number;
}
console.log('squire==>',squire(2));


function devision(number,number2){
	return number / number2;
}
console.log('devision==>',devision(6,3));


function sum(number,number2){
	return number + number2;
}
console.log('sum==>',sum(6,3));



function multiply(number,number2){
	return number * number2;
}
console.log('multiply==>',multiply(6,3));


function subtraction(number,number2){
	return number - number2;
}
console.log('subtraction==>',subtraction(6,3));